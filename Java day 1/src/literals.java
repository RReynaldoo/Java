//Variable = (Literal)

public class literals {
    public static void main(String[] args){

        int binary = 0b1011;            //Add 0b*** for Binary
        int exadecimal = 0x122AE;       //Add 0x*** for Hexa

        int dbl = 100_000_000;          //You can add _ between numbers and it'll help visually and it wont affect the output

        double a = 15e74;               //You can use exponential....something
        System.out.println(a);

        char x = 'a';
        x++;                            //You can get the next variable by adding 1 to the character
        System.out.println(x);


    }
    
}
